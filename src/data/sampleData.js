// Helper function to get products by section
export const getProductsBySection = (sectionId) => {
  return sampleProducts.filter(p => p.section_id === sectionId && !p.parent_id);
};

// Helper function to get child products
export const getChildProducts = (parentId) => {
  return sampleProducts.filter(p => p.parent_id === parentId);
};

// Helper function to calculate promised date
export const calculatePromisedDate = (isExpress = false) => {
  const date = new Date();
  const daysToAdd = isExpress ? serviceSettings.express_completion_days : serviceSettings.default_completion_days;
  date.setDate(date.getDate() + daysToAdd);
  date.setHours(12, 0, 0, 0);
  return date;
};

